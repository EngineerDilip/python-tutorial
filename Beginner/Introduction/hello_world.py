#Using print() function (the most common)
print("Hello,World!!")

#Using string formatter with % (old method)
print("%s" % "Hello World!!")

#Using str.format() method
print("{}".format("Hello World!!"))

#Using f-string ( only python 3.6+)
print(f"Hello World!!")

#Using repr() function
print(repr("Hello World!!"))

#Using join() with print()
print(" ".join(["Hello", "World"]))

#Using Concatenating string inside print()
print("Hello" + "World!!")